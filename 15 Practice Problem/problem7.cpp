#include<iostream>
using namespace std;

int main(){
    cout<<"Display arithmetic operations with mixed data type :"<<endl;
    cout<<"---------------------------------------------------"<<endl;

    cout<<5<<"+"<<7<<" = "<<5+7<<endl;
    cout<<3.7<<"+"<<8.0<<" = "<<3.7+8.0<<endl;
    cout<<5<<"+"<<8.0<<" = "<<5+8.0<<endl;
    cout<<5<<"-"<<7<<" = "<<5-7<<endl;
    cout<<3.7<<"-"<<8.0<<" = "<<3.7-8.0<<endl;
    cout<<5<<"-"<<8.0<<" = "<<5-8.0<<endl;
    cout<<5<<"*"<<7<<" = "<<5*7<<endl;
    cout<<3.7<<"*"<<8.0<<" = "<<3.7*8.0<<endl;
    cout<<5<<"*"<<8.0<<" = "<<5*8.0<<endl;
    cout<<5<<"/"<<7<<" = "<<5/7<<endl;
    cout<<3.7<<"/"<<8.0<<" = "<<3.7/8.0<<endl;
    cout<<5<<"/"<<8.0<<" = "<<5/8.0<<endl;
    
    return 0;
}